    private boolean isValidForm() {
        errorTextLabel.setText("");
        String[] formInputStrings = new String[]{
            productNameInput.getText(), productDescriptionInput.getText(), 
            auctionNameInput.getText(), auctionDescriptionInput.getText(), 
            auctioneerNameInput.getText()
        };
        
        for(String formString : formInputStrings) {
            if (formString.isBlank()) {
                errorTextLabel.setText("Blank input, plase fill all the data");
                return false;
            }
            if (!formString.matches("^[ A-Za-z]+$")) {
                errorTextLabel.setText("Text data should only contain letters!");
                return false;
            }
        }
        
        String[] urls = new String[]{ productImageURLInput.getText(), auctionImageURLInput.getText() };
        for(String url: urls) {
            try {
                new URL(url).toURI();
            } catch(Exception e) {
                errorTextLabel.setText("Invalid image url.");
                return false;
            }
            
        }
        
        Date endDate = (Date) auctionDateInput.getValue();
        if (new Date().after(endDate)) {
            errorTextLabel.setText("End date can't be in the past.");
            return false;
        }
        
        
        return true;
    }

