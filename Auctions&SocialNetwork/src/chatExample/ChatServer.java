    private CmdServer cmdServer;
    
    public ChatServer() {
        initComponents();

        try {
            this.cmdServer = new CmdServer(8080);
            cmdServer.listen(new ChatCmdFactory(), this, this);  // passed as context and as hook onSocketClosed
            cmdServer.setLogging(true);
            
            this.addWindowListener(new WindowAdapter() {
                @Override
                public void windowClosing(WindowEvent windowEvent) {
                    cmdServer.setRunning(false);  // stop all command server threads
                    System.exit(0);
                }
            });
        
        } catch(Exception e) {
            System.out.println(e);
            System.exit(1);
        }
    }

    
    public void updateSendNameDropdown(ArrayList<String> names) {
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>(names.toArray(new String[0]));
        sendNameDropdown.setModel(model);
        sendNameDropdown.setSelectedIndex(0);
    }

    
    public CmdServer getCmdServer() {
        return cmdServer;
    }
    
    public void display(String message) {
        chatTextArea.append(message + "\n");
    }
    
    @Override
    public void onCmdSocketDisconnected(String socketName) {
        new NotifyNamesChangeCmd().execute(this);  // update client names
    }

    
